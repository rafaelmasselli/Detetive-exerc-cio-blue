public class calculator {

  static public int sum(int num, int num2) {
    return num + num2;
  }

  static public int subtraction(int num, int num2) {
    return num - num2;
  }

  static public int multiplication(int num, int num2) {
    return num * num2;
  }

  static public int division(int num, int num2) {
    return num / num2;
  }
}
